/// Map a libGDX key code to its display name.
///
/// Java: com.badlogic.gdx.Input.Keys.toString(int keycode)
pub(super) fn gdx_key_name(keycode: i32) -> &'static str {
    match keycode {
        // Letters A-Z (29..=54)
        29 => "A",
        30 => "B",
        31 => "C",
        32 => "D",
        33 => "E",
        34 => "F",
        35 => "G",
        36 => "H",
        37 => "I",
        38 => "J",
        39 => "K",
        40 => "L",
        41 => "M",
        42 => "N",
        43 => "O",
        44 => "P",
        45 => "Q",
        46 => "R",
        47 => "S",
        48 => "T",
        49 => "U",
        50 => "V",
        51 => "W",
        52 => "X",
        53 => "Y",
        54 => "Z",
        // Numbers
        7 => "0",
        8 => "1",
        9 => "2",
        10 => "3",
        11 => "4",
        12 => "5",
        13 => "6",
        14 => "7",
        15 => "8",
        16 => "9",
        // Arrow keys
        19 => "Up",
        20 => "Down",
        21 => "Left",
        22 => "Right",
        // Punctuation / symbols
        55 => ",",
        56 => ".",
        62 => "Space",
        66 => "Enter",
        67 => "Backspace",
        68 => "`",
        69 => "-",
        70 => "=",
        71 => "[",
        72 => "]",
        73 => "\\",
        74 => ";",
        75 => "'",
        76 => "/",
        // Modifier keys
        57 => "L-Alt",
        58 => "R-Alt",
        59 => "L-Shift",
        60 => "R-Shift",
        61 => "Tab",
        129 => "L-Ctrl",
        130 => "R-Ctrl",
        131 => "Caps Lock",
        // Special keys
        111 => "Escape",
        112 => "Delete",
        133 => "Insert",
        // F keys
        244 => "F1",
        245 => "F2",
        246 => "F3",
        247 => "F4",
        248 => "F5",
        249 => "F6",
        250 => "F7",
        251 => "F8",
        252 => "F9",
        253 => "F10",
        254 => "F11",
        255 => "F12",
        _ => "Unknown",
    }
}
